package game;

/**
 *
 * @author Bue
 */
public class Player {
    private String name;
    private int points;
    
    public Player(String n, int p){
        name = n;
        points = p;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPoints() {
        return points;
    }

    public void setPoints(int points) {
        this.points = points;
    }
    
    
    
}
